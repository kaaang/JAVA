package day08;

public class ArTask {
	public static void main(String[] args) {
		
		int [] ar5 = new int[5];
		int [] ar10 = new int[10];
		
		
		
		for(int i=0;i<5;i++)
		{
			ar5[i] = i+1;
			System.out.println(ar5[i]);
			
		}
		
		
		for (int i = 0; i < ar10.length; i++) {
			ar10[i] = 10-i;
			System.out.println(ar10[i]);
		}
		
		
		
		
		
		
	}
}
